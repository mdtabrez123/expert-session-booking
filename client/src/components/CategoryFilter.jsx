function getMeta(category) {
  return CATEGORY_META[category] || {
    icon: <GenericIcon />,
    color: '#a78bfa',
    glow: 'rgba(167,139,250,0.15)',
  };
}

// ── Component ───────────────────────────────────────────────────────────────
export default function CategoryFilter({ value, onChange, categories = [] }) {
  const scrollRef = useRef(null);
  const [isDragging, setIsDragging] = useState(false);
  const [startX, setStartX]         = useState(0);
  const [scrollLeft, setScrollLeft] = useState(0);

  const tabs   = ['All', ...categories];
  const active = value || 'All';

  // Drag-to-scroll on desktop
  const onMouseDown = (e) => {
    setIsDragging(true);
    setStartX(e.pageX - scrollRef.current.offsetLeft);
    setScrollLeft(scrollRef.current.scrollLeft);
  };
  const onMouseMove = (e) => {
    if (!isDragging) return;
    e.preventDefault();
    const x    = e.pageX - scrollRef.current.offsetLeft;
    const walk = (x - startX) * 1.2;
    scrollRef.current.scrollLeft = scrollLeft - walk;
  };
  const onMouseUp = () => setIsDragging(false);

  return (
    <div
      ref={scrollRef}
      className="flex items-center gap-1.5 overflow-x-auto py-2 px-2 select-none"
      style={{ scrollbarWidth: 'none', msOverflowStyle: 'none', cursor: isDragging ? 'grabbing' : 'grab' }}
      onMouseDown={onMouseDown}
      onMouseMove={onMouseMove}
      onMouseUp={onMouseUp}
      onMouseLeave={onMouseUp}
    >
      {tabs.map((cat) => {
        const isActive = cat === active;
        const meta     = getMeta(cat);

        return (
          <button
            key={cat}
            onClick={() => onChange(cat === 'All' ? '' : cat)}
            className="relative flex-shrink-0 flex items-center gap-2 px-3.5 py-2 rounded-xl text-xs font-medium transition-all duration-200 whitespace-nowrap outline-none focus-visible:ring-2 focus-visible:ring-violet-500"
            style={
              isActive
                ? {
                    background: `rgba(255,255,255,0.06)`,
                    border: `1px solid rgba(255,255,255,0.12)`,
                    color: '#f4f4f5',
                    boxShadow: `0 0 12px ${meta.glow}, inset 0 1px 0 rgba(255,255,255,0.08)`,
                  }
                : {
                    background: 'transparent',
                    border: '1px solid transparent',
                    color: '#52525b',
                  }
            }
          >
            {/* Sliding background indicator */}
            {isActive && (
              <motion.span
                layoutId="activeCategoryBg"
                className="absolute inset-0 rounded-xl"
                style={{ background: `linear-gradient(135deg, ${meta.glow}, transparent)` }}
                transition={{ type: 'spring', stiffness: 380, damping: 32 }}
              />
            )}

            {/* Icon */}
            <span
              className="relative z-10 flex-shrink-0 transition-colors duration-200"
              style={{ color: isActive ? meta.color : '#3f3f46' }}
            >
              {meta.icon}
            </span>

            {/* Label */}
            <span
              className="relative z-10 transition-colors duration-200"
              style={{ color: isActive ? '#e4e4e7' : '#52525b' }}
            >
              {cat}
            </span>

            {/* Active dot indicator */}
            {isActive && cat !== 'All' && (
              <motion.span
                initial={{ scale: 0 }}
                animate={{ scale: 1 }}
                className="relative z-10 h-1.5 w-1.5 rounded-full flex-shrink-0"
                style={{ background: meta.color }}
              />
            )}
          </button>
        );
      })}
    </div>
  );
}
